/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  AHT10_Init(&hi2c1);
  LCD_init(&hi2c1);
  /* USER CODE BEGIN 2 */
  LCD_clear();
  LCD_setCursor(0, 0);
  LCD_print("Iniciando...");
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	  switch (estado)
	   {
	       case INITIALIZATION:
	           t_prev = HAL_GetTick();
	           estado = verify_sensors;
	           break;

	       case verify_sensors:
	           estado = Read_start;
	           break;

	       case Read_start:
	           if (HAL_GetTick() - t_prev >= periodo)
	           {
	               estado = Read_wait;
	           }
	           break;

	       case Read_wait:
	    	   if (AHT10_LeerDatos(&hi2c1, &sensor))
	    	     {
	    	         temperatura = sensor.temperature;
	    	         estado = process_data;
	    	     }
	    	     else
	    	     {
	    	         estado = STATE_ERRORI2C;
	    	     }
	           break;

	       case process_data:
	           estado = update_display;
	           break;

	       case update_display:
	    	    int temp_int = (int)sensor.temperature;
	    	    int temp_dec = (int)((sensor.temperature - temp_int) * 100);

	    	    if (temp_dec < 0) temp_dec *= -1;

	    	    LCD_setCursor(0,0);
	    	    snprintf(buffer, sizeof(buffer), "Temp:%2d.%02d C", temp_int, temp_dec);
	    	    LCD_print(buffer);

	    	    if (sensor.temperature >= 30.0f)
	    	    {
	    	        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);

	    	        LCD_setCursor(1,0);
	    	        LCD_print("ALERTA TEMP ALTA");
	    	    }
	    	    else if(sensor.temperature < 20.0f)
	    	    {
	    	    	HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);

	    	    	LCD_setCursor(1,0);
	    	    	LCD_print("TEMP FRIA       ");
	    	    }
	    	    else
	    	    {
	    	        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);

	    	        LCD_setCursor(1,0);
	    	        LCD_print("Temp Normal     ");
	    	    }

	    	    t_prev = HAL_GetTick();
	    	    estado = Read_start;
	    	    break;


	       case STATE_ERRORI2C:
	           LCD_clear();
	           LCD_setCursor(0,0);
	           LCD_print("Error I2C");
	           break;

	       default:
	           estado = INITIALIZATION;
	           break;
	   }
    /* USER CODE BEGIN 3 */
  }

  /* USER CODE END 3 */
}

